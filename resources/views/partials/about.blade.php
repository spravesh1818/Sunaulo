  <h2 style="color:#EF008C;">बालिका दुलही होइनन् , राष्ट्रिय संजाल नेपाल</h2>

  <img style="border: 5px;" src="{{ URL::asset('images/girls.png')}}" >

  <h3 style="color:#777;">'गर्ल्स नट ब्राइटस' वा 'बालिका दुलही होइनन्' भन्ने बालविवाह बिरुद्धको अन्तराष्ट्रिय अभियान हो ।  यो विश्वका ७० मुलुकका करिब ४ सय भन्दा बढी गैरसरकारी संस्था र नागरिक समाजको संजाल हो ।  यसले बालविवाह अन्त्य गरी बालबालिका खास गरी बालिकाको ससक्तिकरण गरी उनीहरुलाई सक्षम् बनाउने उदेश्य राख्छ। संजालले वालविवाहको अन्त्यको लागि सबै खालका प्रयासहरु गर्छ । संसारका धेरै मुलुकमा वालविवाहको अन्त्यको राष्ट्रिय रणनीति बनाउने लगायतका सरकारी तथा गैर सरकारी प्रयासहरुलाई एकीकृत रुपमा प्रभावकारी ढङ्गले लागु गर्ने काममा सक्रिय छ । नेपालमा पनि 'बालिका दुलही होइनन्' भन्ने राष्ट्रिय संजाल पाँच वर्ष देखि (सन् २०१२) कार्यरत छ । यसले बालविवाह अन्त्यको राष्ट्रिय रणनीति २०७२ र त्यससंगसम्बन्धित राष्ट्रिय कार्ययोजना  तयार गर्ने महत्वपूर्ण कामको  अगुवाई गरेको छ । बाल विवाहको अन्त्य र महिला हिंसाको विरुद्धमा काम गर्ने कुनै पनि गैर सरकारी संस्था यो संजालको सदस्य बन्न सक्छन् । यसमा आबद्ध सबै संस्था स्वयम् सेवाका रुपमा बालविवाहको अन्त्यको महान अभियानमा जुटेका छन्।</h3>
