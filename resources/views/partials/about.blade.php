  <h3 cladd="right"style="color:#777;"> बिकास र मानवीय सहायता संस्थाको रूपमा सन् १९३६ मा स्थापना भएको प्लान इन्टरनेसनलले बाल अधिकार र किशोरीहरूको समानताको पक्षमा हाल विश्वका ७१ राष्ट्रमा कार्यरत छ । नेपालमा पनि करिब ४० वर्ष देखि यो संस्था निरन्तर रुपमा बालबालिकाको गुणस्तरीय शिक्षा, आर्थिक अवसर र बाल संरक्षण प्राप्तिको लागि समुदाय, परिवार र बालबालिकाहरूसँग हातेमालो गरेर काम गरिरहेको छ । गरिब, सिमान्तकृत र पिछडिएका वर्गका बालबालिका र किशोरीहरूको पक्षमा राष्ट्रिय तथा अन्तरास्ट्रिय नीति तथा कार्यक्रम तय गर्ने, तिनीहरुको सफल कार्यन्वयन गराउने कार्यमा निरन्तर लागिरहेको यो संस्थाले समुदायमा रहेका बालबालिका युवा तथा महिलाको  शशक्तिकरण र उनीहरुको अर्थपूर्ण सहभागितालाई प्रोत्साहन गर्छ । 
प्लान इन्टरनेसनलले बालबालिकालाई खासगरी बालिकाहरुको समानता लगायत बालअधिकार प्राप्तिका लागि 'सिक, नेतृत्व गर, निर्णय गर र समृद्ध जीवन बाँच' भन्ने उदघोषकासाथ सशक्तीकरण गर्न प्रतिबद्ध छ।
यसै अनुरुप समाजमा विद्यमान सामाजिक कुरीति, कुसंस्कारको रुपमा रहेको महिलामाथि हुने हिंसा भेदभाव र बालविवाह बिरुद्ध सम्पूर्ण सरोकारवालालाई गोलबद्ध गरी शसक्त आवाज उठाउने काममा प्लानको महत्वपुर्ण योगदान रहेको छ । 
“गरिबी, अशिक्षा र धर्म कमाउने नाममा हुने बालविवाह केहि समय यता देखि भने आफू खुसी बिबाह गर्ने कारण बढेको देखिन्छ । यहि विषयवस्तुलाई ध्यानमा राखेर किशोरकिशोरीहरुको समग्र बिकासको लागि उनीहरुको शशक्तिकरण गर्ने उदेश्यकासाथ बालविवाहको अन्त्यलाई मूल ध्येय बनाएर तयार गरिएको यो पोर्टलको साझेदार बनेको छ”-प्लान इन्टरनेशनल नेपाल ।

