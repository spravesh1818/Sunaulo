  <h2 style="color:#EF008C;">यो अनलाइन पोर्टल किन ?</h2>

  
  <h3 cladd="right"style="color:#777;"> 'सहि उमेर सहि कदम' ले नै संकेत गर्ला । जुन उमेरमा जे गर्नु पर्ने हो तेही गर्नु उपयुक्त हुन्छ । सानो उमेरमा मनोरन्जन र ज्ञान आर्जन गर्ने हो। भविष्यको लागि आवश्यक ज्ञान, सिप र  उर्जा विकास गर्ने समयमा विवाह गर्ने चलन वा प्रवृतिले लाखौ बालबालिकाको भविष्य अन्धकार भएको छ । स्वास्थ्य, शिक्षा र रोजगारीको अवसरहरू गुमाएर अभाव, पीडा र गरिबीसँग संघर्ष गर्दै जीवनयापन गर्नुपर्ने बाध्यता छ ।
कानुनले २० वर्ष पूरा गरेपछि मात्र विवाहका लागि योग्य मानिएको भए पनि अझै ३७ प्रतिशतको विवाह १८ वर्ष नपुग्दै हुने गरेको छ। बालविवाह, बाल अधिकारको हनन् मात्र नभई यसले बैवाहिक जीवन दिगो नहुने, परिपक्व नभई सन्तान जन्माउदा स्वास्थ्यसम्बन्धी विभिन्न समस्याहरु देखिने, लैंगिक हिंसा,यौनजन्य हिंसा, बालश्रम, वेचविखन जस्ता थप हिंसाहरुले बालिका र महिलाहरु थप प्रताडित हुन पुग्दछन् ।
 
बालविवाह अथवा १८ वर्ष भन्दा कम उमेरमा गरिने विवाहको दर घट्दै गएको देखिए पनि नेपाल दक्षिण एसियाली मुलुकहरुको दाँजोमा उच्च बालविवाह हुने मुलुकहरु मध्ये तेस्रो र विश्वमा २२ औ स्थानमा पर्छ । हिजो गरिबी, अशिक्षा र धर्म कमाउने नाममा अभिभावाकहरूले नै सानै उमेरमा विवाह गरिदिन्थे तर आज आफु खुशी सानै उमेरमा विहे गर्ने चलन बढेको छ । ग्रामीण भेगमा विशेषगरी अभिभावकले नै र शहरी वा अर्धशहरी क्षेत्रमा आफू खुसी विवाह गर्ने हुँदा समग्रमा बालविवाहको दर बढ्ने देखिन्छ । यसरी आफू खुसी विवाह गर्नुको मुख्य कारण युवा किशोरकिशोरिहरुमा यौन तथा प्रजनन् स्वास्थ्य, यौन र यौनिकतासम्बन्धी न्युन ज्ञान र यी विषयहरुमा ज्ञानआर्जन गर्ने सन्दर्भ सामग्रीहरुको अभाव हुनु हो । यीनै कुराहरूलाई ध्यानमा राखेर बालविवाह अन्त्यको लागि किशोर किशोरीहरुलाई शसक्तिकरण गर्ने उदेश्यकासाथ  यो पोर्टल तयार गरिएको हो । 
यो पोर्टललाई नियमित रुपमा अद्यावधिक गरिनेछ । बालविवाह, लैङ्गिक हिंसा, यौन, यौनिकता, प्रजनन् स्वास्थ्य, शिक्षा लगायतका बालवालिकाको सुन्दर भविष्यको लागि आवश्यक सम्पूर्ण पाठ्य सामग्रीहरु राखिनेछन् । युवाकिशोकिशोरीहरुको जिज्ञासा र खुल्दुलीहरुको समाधानको लागि छलफल, प्रश्नोत्तर र हाजिरी जवाफ जस्ता शसक्त माध्यमहरुको प्रयोग गरिनेछन् ।
यो पोर्टल प्लान इन्टरनेसनल नेपाल, बालिका दुलही होइननन् राष्ट्रिय संजाल र सोलिड नेपालको सहकार्यमा निर्माण गरिएको हो  र यसलाई सोलिड नेपालले निरन्तर रुपमा सम्पादन र अद्यावधिक गर्दै जाने छ ।
सोलिड नेपालको तर्फबाट यौन तथा प्रजनन् स्वास्थ्य बिज्ञ डा. खेम बहादुर कार्कीले नेतृत्व गरेको टिमले अनलाइनपोर्टल सम्पादन र अद्यावधिक गर्नेछ ।   


