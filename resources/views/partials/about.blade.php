  <h2 style="color:#EF008C;">स्थानीय एकीकृत विकाश समाज नेपाल ( सोलिड नेपाल ) </h2>

<img style="border: 5px;" src="{{ URL::asset('images/solid.png')}}" >

  <h3 style="color:#777;">मुनाफा रहित गैर सरकारी संस्थाको रुपमा आजभन्दा करिब २० बर्ष अघि (सन् १९९७) स्थापित, निरन्तर र निष्ठापूर्ण ढंगले युवाकिशोरकिशोरीको सशक्तिकरण मार्फत समग्र समाज विकासमा कार्यरत संस्थाको हो - स्थानीय एकीकृत विकास समाज नेपाल (सोलिड नेपाल)। यो संस्थाको निर्देशक सिदान्त भनेको समाज विकासका लागि बाधक सामाजिक, साँस्कृतिक समस्याहरूको सुक्ष्म अध्ययन गर्ने,एकीकृत कार्ययोजना तर्जुमा गर्ने र स्थानीय साधनस्रोतको अधिकतम् उपयोग गरी जनताको अर्थपूर्ण सहभागितामा दिगो विकास गर्ने हो।
युवाकिशोरकिशोरीको यौन तथा प्रजनन् स्वास्थ्य स्थिति सुधार्न सानो उमेरमा हुने विवाह रोक्नु पर्छ ।बालविवाहले किशोरकिशोरीको स्वास्थ्य, शिक्षा, रोजगारी, सामाजिक नेतृत्व लगायतका विकास र उन्नतिका सम्पूर्ण अवसरहरूबाट उनीहरूलाई वन्चित गर्छ। नेपाल विश्वमा बालविवाह उच्च रहने मुलुकहरू मध्ये एक हो । सोलिड नेपाल बालविवाह जस्ता सामाजिक कुरीति बिरुद्ध लड्ने एक अगुवा संस्था हो । यति मात्रै होइन, संस्थाले बढ्दै गईरहेको नसर्ने रोग नियन्त्रण तथा रोकथाममा पनि अग्रणी भूमिका निर्वाह गरिरहेको छ। </h3>
